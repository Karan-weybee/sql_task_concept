CREATE FUNCTION RANKER_STU(
@NAME VARCHAR(20)
)
RETURNS TABLE
AS
RETURN (
SELECT * FROM STUDENTS WHERE NAME=@NAME
)

SELECT * FROM DBO.RANKER_STU('HARSH')


CREATE FUNCTION RANKER (
@MARKS INT
)
RETURNS TABLE
AS 
RETURN (
SELECT * FROM STUDENTS WHERE MARKS>=@MARKS
);

SELECT * FROM DBO.RANKER(80);

CREATE FUNCTION ADD_FIVE_MARK(
@MARK INT
)
RETURNS INT 
AS 
BEGIN 
RETURN (
@MARK+5
)
END;

SELECT DBO.ADD_FIVE_MARK(550)

CREATE FUNCTION GET_NAME (
@ID INT
)
RETURNS VARCHAR(20)
AS
BEGIN
RETURN (
SELECT NAME FROM STUDENTS WHERE ID=@ID
)
END;

SELECT DBO.GET_NAME(3)
